"""
Perform simple math add, subtract , multiplication and divide with two variables.
"""

a = float(input("Enter first number: "))
b = float(input("Enter second number: "))

# add two numbers
print(a+b)
# subtract two numbers
print(a-b)
# multiply two numbers
print(a*b)
# divide two numbers
print(a/b)
# remainder of two numbers
print(a%b)
# power
print(a**2)
# grater than
print(a>b)